    if not access_token:
        return {"error": "failed to get token"}
    
    params = {
        "receiptId": receipt_id,
        "_key": "hkZ0wRxfCpE43aua"
    }
    headers = {
        "x-forwarded-prefix": "gateway",
        "Authorization": f"Bearer {access_token}",
        "Content-Type": "application/json"
    }
    
    logger.info(f"📤 查询发票: {receipt_id}")
    
    try:
        response = session.get(
            API_URL,
            params=params,
            headers=headers,
            verify=False,
            timeout=30
        )
        
        if response.status_code == 200:
            try:
                result = response.json()
                # 处理布尔值响应（如 true/false）
                if isinstance(result, bool):
                    logger.debug(f"Response: {result}")
                    return {"result": result}
                logger.debug(f"Response: {json.dumps(result, ensure_ascii=False)[:200]}")
                return result
            except json.JSONDecodeError:
                logger.warning(f"⚠️ JSON 解析失败，响应: {response.text[:100]}")
                return {"raw": response.text}
        else:
            logger.error(f"❌ HTTP {response.status_code}: {response.text[:100]}")
            return {"error": f"HTTP {response.status_code}"}
            
    except Exception as e:
        logger.error(f"🌐 请求异常: {e}")
        return {"error": str(e)}


