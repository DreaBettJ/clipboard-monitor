class Logger:
    def __init__(self, log_file):
        self.log_file = log_file
        self.log_file.write_text("", encoding="utf-8")
        self.start_time = datetime.now()
        
    def log(self, level: str, msg: str):
        """带颜色的日志输出"""
        timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        elapsed = (datetime.now() - self.start_time).total_seconds()
        formatted = f"[{timestamp}] [{elapsed:6.1f}s] [{level:8}] {msg}"
        
        colors = {
            "INFO": "\033[94m",
            "SUCCESS": "\033[92m",
            "WARNING": "\033[93m",
            "ERROR": "\033[91m",
            "TOKEN": "\033[95m",
        }
        reset = "\033[0m"
        color = colors.get(level, "")
        print(f"{color}{formatted}{reset}")
        
        with open(self.log_file, "a", encoding="utf-8") as f:
            f.write(f"[{timestamp}] [{level:8}] {msg}\n")
    
    def info(self, msg): self.log("INFO", msg)
    def success(self, msg): self.log("SUCCESS", msg)
    def warning(self, msg): self.log("WARNING", msg)
    def error(self, msg): self.log("ERROR", msg)
    def token(self, msg): self.log("TOKEN", msg)
    def debug(self, msg): self.log("DEBUG", msg)


class TokenManager:
    """Token 管理器 - 自动获取和刷新 Token"""
    
    def __init__(self, logger: Logger):
        self.logger = logger
        self.access_token = None
        self.expires_at = None
    
    def get_token(self) -> str:
        """获取新的 Access Token"""
        self.logger.token("🔑 正在获取 Access Token...")
        
        headers = {
            "Authorization": AUTH_HEADER,
            "x-forwarded-prefix": "gateway"
        }
        
        data = {
            "grant_type": "client_credentials",
            "scope": "write"
        }
        
        try:
            response = requests.post(
                TOKEN_URL,
                headers=headers,
                data=data,
                verify=False,
                timeout=30
            )
            
            if response.status_code == 200:
                result = response.json()
                self.access_token = result["access_token"]
                expires_in = result.get("expires_in", 7200)
                self.expires_at = datetime.now().timestamp() + expires_in
                
                self.logger.token(f"✅ Token 获取成功 (有效期: {expires_in}秒)")
                self.logger.token(f"📝 Token: {self.access_token[:50]}...")
                
                return self.access_token
            else:
                self.logger.error(f"❌ Token 获取失败: {response.status_code}")
                self.logger.error(f"响应: {response.text[:200]}")
                return None
                
        except Exception as e:
            self.logger.error(f"🌐 Token 请求异常: {e}")
            return None
    
    def get_valid_token(self) -> str:
        """获取有效的 Token（自动刷新）"""
        # 检查是否需要刷新
        if self.access_token and self.expires_at:
            remaining = self.expires_at - datetime.now().timestamp()
            if remaining > 60:  # 剩余超过60秒，继续使用
                return self.access_token
        
        return self.get_token()


